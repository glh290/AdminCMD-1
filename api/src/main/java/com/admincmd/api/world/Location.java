package com.admincmd.api.world;

public class Location {

    private double x;
    private double y;
    private double z;
    private float yaw;
    private float pitch;
    private World world;

    public Location(double x, double y, double z, World w) {
        this(x, y, z, 0, 0, w);
    }

    public Location(double x, double y, double z, float yaw, float pitch, World w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.yaw = yaw;
        this.pitch = pitch;
        this.world = w;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

    public float getYaw() {
        return yaw;
    }

    public float getPitch() {
        return pitch;
    }

    public World getWorld() {
        return world;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public void setYaw(float yaw) {
        this.yaw = yaw;
    }

    public void setPitch(float pitch) {
        this.pitch = pitch;
    }

    @Override
    public String toString() {
        return getClass().getPackage().getName() + "." + getClass().getName() + "{" + "x=" + x + ";y=" + y + ";z=" + z + "world=" + world.getName() + "}";
    }

}
